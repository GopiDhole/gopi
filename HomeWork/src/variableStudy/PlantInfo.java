package variableStudy;

public class PlantInfo {

	public static void main(String[] args) {
		//Information of Air Purifier Indoor Plants
		//1.Snake Plant-->Kingdom-->Colour of leaves-->length of leaves-->width of leaves-->height of plant-->
		
		String name="Snake Plant"; //variable declaration  and assign value
		String kingdom="Plantae"; //variable declaration  and assign value
		String colOfLeaves="Dark Green with light grey"; //variable declaration  and assign value
		int heightOfPlant=2; //variable declaration  and assign value
		int lengthOfLeaves=90; //variable declaration  and assign value
		int widthOfLeaves=6; //variable declaration  and assign value
		
		System.out.println("===================Plant Information=============================");
		System.out.println("The name of the plant is             :"+name);
		System.out.println("Name of the kingdom of the plant is  :"+kingdom);
		System.out.println("Colour of the Leaves is              :"+colOfLeaves);
		System.out.println("Length of the plant                  :"+heightOfPlant +" m");
		System.out.println("Length of the leaves                 :"+lengthOfLeaves +" cm");
		System.out.println("Width of the leaves                  :"+widthOfLeaves +" cm");
		System.out.println("=================================================================");
		
		
	}

}
